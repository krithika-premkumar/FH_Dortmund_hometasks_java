package sixthtask;

public class Manufacture {
	private long year;
	private String model;
	private String colour;

	long getManufactureyear()
	{
		return year;
		
	}
	String getmodel()
	{
		return model;
	}
	String getcolour()
	{
		return colour;
	}
	
	void setManufactureyear(long y)
	{
		this.year = y;
	}
	void setModel(String y)
	{
		this.model = y;
	}
	void setcolour(String y)
	{
		this.colour = y;
	}
}
